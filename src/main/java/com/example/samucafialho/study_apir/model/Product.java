package com.example.samucafialho.study_apir.model;

public class Product {
    private long id;
    private String nome;

    public void setId(long id) {
        this.id = id;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public long getId() {
        return id;
    }

    public String getNome() {
        return nome;
    }

}
